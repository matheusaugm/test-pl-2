import java.util.*;

public class Consulta {
	int horariosMarcados;
	int possiveisHorarios;
	String dia;

	public int getHorariosMarcados() {
		return horariosMarcados;
	}

	public void setHorariosMarcados(int horariosMarcados) {
		this.horariosMarcados = horariosMarcados;
	}

	public int getPossiveisHorarios() {
		return possiveisHorarios;
	}

	public void setPossiveisHorarios(int possiveisHorarios) {
		this.possiveisHorarios = possiveisHorarios;
	}

	public String getDia() {
		return dia;
	}

	public void setDia(String dia) {
		this.dia = dia;
	}
}
